<h2 style="color:#DC4796; font-weight:bold; font-size:13px; text-align:left;">L'équipe de mediums et voyants auditifs My Astro s'engage à respecter avec rigueur la présente charte de déontologie
:</h2>
<h3>Article 1 – Sécurité</h3>
<p>L’équipe My Astro travaille avec un centre de gestion qui procède systématiquement à la sécurisation de vos coordonnées bancaires avant de vous mettre en relation avec un consultant. Ces
coordonnées seront détruites après acquittement du montant de la consultation. Vous serez débité par le système de paiement Blue Paid ou SysPay, principalement par les clients belges.</p>
<h3>Article 2 – Qualité</h3>
<p>L'équipe My Astro se porte garante de la fiabilité des professionnels de la voyance qu’elle a sélectionné avec la plus grande attention pour vous. Leurs engagements sont de vous apporter leur
savoir faire et vous offrir une prestation de qualité avant tout.</p>
<h3>Article 3 – Eléments confidentiels</h3>
<p>Tous les éléments qui seront traités au cours de la consultation resteront dans un cadre totalement confidentiel dans lequel vous pourrez vous exprimer en toute confiance. L’Equipe My Astro
s’engage à ne divulguer aucune de ces informations.</p>
<h3>Article 4 – Sincérité</h3>
<p>L’équipe My Astro s’engage à mettre en place une consultation de voyance en toute honnêteté. Cet engagement concerne la fidélité des propos soutenus par le consultant et la garantie de ne pas vous
diriger vers un thème ou un sujet opposé à celui pour lequel vous le contacter. L’équipe My Astro forme un ensemble de professionnels de la voyance dont l’expérience permet une compréhension totale
des éléments apportés de manière explicite et concise. Son travail sera étayé exclusivement sous forme d’informations et de conseils.</p>
<h3>Article 5 – Vigilance</h3>
<p>L’équipe My Astro s’engage à rester vigilent dans ses réponses. Les spécialistes sont là pour vous apporter leur aide, leur soutien et leur expérience mais en aucun cas pour faire le tri entre le
positif et le négatif ou encore vous dire ce que vous avez envie d’entendre. Ceci afin de ne pas vous donner de faux espoirs, ni de porter un regard sur vous ou sur une autre personne et ni de faire
des prédictions improbables.</p>
<h3>Article 6 – Ethique</h3>
<p>L’équipe My Astro se prive de répondre aux sujets contraires à l’éthique et aux bonnes mœurs. C’est la raison pour laquelle les questions liées au domaine de la santé (maladies graves,
diagnostics, etc.) et celles liées au domaine de la mort ne seront jamais envisagées.</p>
<h3>Article 7 – Respect des mineurs</h3>
<p>L’équipe My Astro s’engage à ne pas mettre en place une consultation de voyance avec une personne âgée de moins de 18 ans ainsi qu’avec toute personne estimée faible ou fragile
psychologiquement.</p>
<h3>Article 8 – Valeur</h3>
<p>L’équipe My Astro s’engage à ne pas mettre en difficulté financière ses clients en n'abusant pas du temps supplémentaire lors des consultations ainsi qu'à ne pas dénigrer le travail d'un confrère afin d'attirer un client.</p>
<h3>Article 9 – Honnêteté</h3>
<p>Enfin, l’Equipe My Astro se réserve le droit de reporter à plus tard ou d’annuler une consultation si aucun ressenti n’est présent en ce qui vous concerne.</p>
